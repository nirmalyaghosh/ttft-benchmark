def _generate_system_prompt(target_tokens: int) -> str:
    """
    Generate a system prompt of approximately
    target_tokens length. Uses realistic instruction
    content padded to target length.
    Same word-to-token ratio as _generate_prompt().
    """
    prefix_lines = [
        "You are a helpful AI assistant.",
        "Your role is to provide clear, accurate",
        "and well-structured responses.",
        "Follow these guidelines:\n",
    ]
    prefix_text = " ".join(prefix_lines)
    num_words = _count_words(text=prefix_text)

    words_needed = int(target_tokens * 0.75) - num_words
    padding = (
        "When answering questions, provide "
        "context and explain your reasoning "
        "step by step. If you are unsure about "
        "something, say so clearly. Always cite "
        "sources when making factual claims. "
    )
    padding_words = _count_words(text=padding)
    repetitions = max(
        words_needed // padding_words, 0
    )

    return prefix_text + (padding * repetitions)


def _generate_user_queries(count: int) -> List[str]:
    """
    Generate distinct short user queries for
    prefix caching benchmark. Each query is
    unique to avoid query-level caching.
    """
    return [
        f"Respond with only 'OK'. (#{i + 1})"
        for i in range(count)
    ]


