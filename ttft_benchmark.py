def main() -> None:
    parser = argparse.ArgumentParser(
        description="TTFT Micro-Benchmark",
    )
    parser.add_argument(
        "--prefix-caching",
        action="store_true",
        help=(
            "Run prefix caching benchmark "
            "instead of standard TTFT benchmark"
        ),
    )
    args = parser.parse_args()

    is_prefix_caching = getattr(
        args, "prefix_caching", False
    )

    if is_prefix_caching:
        print("Starting prefix caching "
              "TTFT benchmark...\n")
    else:
        print("Starting TTFT benchmark...\n")

    benchmark_run_dt = datetime.now()
    timestamp = benchmark_run_dt.strftime(
        "%Y%m%d_%H%M%S"
    )

    if is_prefix_caching:
        filename = (
            f"ttft_prefix_caching_{timestamp}.txt"
        )
    else:
        filename = (
            f"ttft_benchmark_{timestamp}.txt"
        )

    # Construct the results file path
    results_dir = _determine_results_directory()
    results_file_path = (
        Path(results_dir) / filename
    )
    results_file_path.parent.mkdir(
        parents=True, exist_ok=True
    )

    try:
        with open(
            results_file_path,
            "w",
            encoding="utf-8",
        ) as results_file:
            if is_prefix_caching:
                results, run_id_base = (
                    run_prefix_caching_benchmark(
                        results_file=results_file,
                        benchmark_run_dt=(
                            benchmark_run_dt
                        ),
                    )
                )
                cost_summary = (
                    _calculate_and_display_cost(
                        run_id_base=run_id_base,
                    )
                )
                _display_prefix_caching_results(
                    results=results,
                    cost_summary=cost_summary,
                    results_file=results_file,
                )
            else:
                results, run_id_base = (
                    run_benchmark(
                        results_file=results_file,
                        benchmark_run_dt=(
                            benchmark_run_dt
                        ),
                    )
                )
                cost_summary = (
                    _calculate_and_display_cost(
                        run_id_base=run_id_base,
                    )
                )
                _display_results(
                    results=results,
                    cost_summary=cost_summary,
                    results_file=results_file,
                )

        print(
            f"\nResults saved to "
            f"{results_file_path}"
        )

    except KeyboardInterrupt:
        print("\n\nBenchmark interrupted by user.")
    except Exception as e:
        print(
            f"\n\nBenchmark failed with error: {e}"
        )


if __name__ == "__main__":
    main()
