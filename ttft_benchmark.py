def _calculate_and_display_cost(run_id_base: str) -> str:
    """
    Helper function used to calculate total tokens used for this benchmark
    run by parsing the JSONL log file.
    """
    log_file_path = os.getenv("LOG_FILE_PATH", "ttft_benchmark_data.jsonl")
    if not os.path.exists(log_file_path):
        print(f"\nLog file {log_file_path} not found for cost calculation.")
        return ""

    total_input_tokens = 0
    total_output_tokens = 0
    total_tokens = 0
    model_used = ""  # Must be read from logs

    with open(log_file_path, "r", encoding="utf-8") as f:
        for line in f:
            try:
                entry = json.loads(line.strip())
                if (entry.get("event") == "api_response" and
                        entry.get("run_id", "").startswith(run_id_base + "-")):
                    usage = entry.get("usage", {})
                    total_input_tokens += usage.get("input_tokens", 0)
                    total_output_tokens += usage.get("output_tokens", 0)
                    total_tokens += usage.get("total_tokens", 0)
                    model_used = entry.get("model", MODEL)
            except json.JSONDecodeError:
                continue  # Skip malformed lines

    cost_summary_str = "\n" + "="*80
    cost_summary_str += "\nCOST SUMMARY"
    cost_summary_str += "\n" + "="*80
    cost_summary_str += f"\nRun ID Base: {run_id_base}"
    cost_summary_str += f"\nModel Used: {model_used}"
    cost_summary_str += f"\nTotal Input Tokens: {total_input_tokens:,}"
    cost_summary_str += f"\nTotal Output Tokens: {total_output_tokens:,}"
    cost_summary_str += f"\nTotal Tokens: {total_tokens:,}"
    cost_summary_str += "\n" + "="*80
    print(cost_summary_str)

    # Note: Pricing calculation depends on provider and model
    # e.g., for GPT-4, input cost ~$0.03/1K tokens, output ~$0.06/1K tokens

    return cost_summary_str


def _display_prefix_caching_results(
        results: Dict[int, Dict[str, Any]],
        cost_summary: str,
        results_file: Optional[Any] = None,
) -> None:
    """
    Display prefix caching benchmark results
    with cache-miss vs cache-hit comparison.
    """
    lines = []
    lines.append("=" * 72)
    lines.append("RESULTS")
    lines.append("=" * 72)
    lines.append("")

    header = (
        f"{'Prefix':<10} {'Miss':<10} "
        f"{'Hit P50':<10} {'Hit P95':<10} "
        f"{'Reduc.':<10} {'Cached':<10}"
    )
    lines.append(header)
    lines.append("-" * 72)

    for size in sorted(results.keys()):
        r = results[size]
        miss_ttft = r["miss"]["ttft"]
        hits = r["hits"]

        if miss_ttft and hits["p50"]:
            reduction = (
                (miss_ttft - hits["p50"])
                / miss_ttft * 100
            )
            reduc_str = f"{reduction:.1f}%"
        else:
            reduc_str = "N/A"

        cached_count = sum(
            1 for c in hits["cached_tokens"]
            if c > 0
        )
        total_hits = len(hits["cached_tokens"])
        cached_str = f"{cached_count}/{total_hits}"

        miss_str = (
            f"{miss_ttft:.3f}s"
            if miss_ttft else "FAILED"
        )
        p50_str = (
            f"{hits['p50']:.3f}s"
            if hits["p50"] else "N/A"
        )
        p95_str = (
            f"{hits['p95']:.3f}s"
            if hits["p95"] else "N/A"
        )

        lines.append(
            f"{size:<10} {miss_str:<10} "
            f"{p50_str:<10} {p95_str:<10} "
            f"{reduc_str:<10} {cached_str:<10}"
        )

    # Calculated vs Measured comparison
    lines.append("")
    lines.append("=" * 72)
    lines.append("CALCULATED vs MEASURED")
    lines.append("=" * 72)
    lines.append("")

    user_query_tokens = ESTIMATED_USER_QUERY_TOKENS
    calc_hdr = (
        f"{'Prefix':<10} {'Calc %':<12} "
        f"{'Measured %':<12} {'Delta':<10}"
    )
    lines.append(calc_hdr)
    lines.append("-" * 72)

    for size in sorted(results.keys()):
        r = results[size]
        miss_ttft = r["miss"]["ttft"]
        hits = r["hits"]

        calc_red = (
            (size - user_query_tokens)
            / size * 100
        )

        if miss_ttft and hits["p50"]:
            measured = (
                (miss_ttft - hits["p50"])
                / miss_ttft * 100
            )
            delta = measured - calc_red
            lines.append(
                f"{size:<10} "
                f"{calc_red:<12.1f} "
                f"{measured:<12.1f} "
                f"{delta:+.1f}pp"
            )
        else:
            lines.append(
                f"{size:<10} "
                f"{calc_red:<12.1f} "
                f"{'N/A':<12} "
                f"{'N/A':<10}"
            )

    lines.append("")
    lines.append(
        "Note: Calculated assumes 100% of prefix"
        " tokens cached; only user query"
    )
    lines.append(
        f"tokens (~{user_query_tokens}) incur "
        "prefill cost. Measured includes"
    )
    lines.append(
        "network latency, infrastructure "
        "variance, and partial cache effects."
    )

    if cost_summary and len(cost_summary) > 0:
        lines.append(cost_summary)

    # Notes section
    lines.append("")
    lines.append("=" * 72)
    lines.append("NOTES")
    lines.append("=" * 72)

    provider = _identify_provider()
    model_region = _get_model_deployment_region()
    model_str = (
        f"{MODEL} via {provider}{model_region}"
    )
    api_client_geo = os.getenv(
        "API_CLIENT_GEOGRAPHY", "Singapore"
    )

    lines.append(
        "- Measurements include network latency"
    )
    lines.append(
        "- Results vary based on server load"
    )
    lines.append(f"- Model: {model_str}")
    lines.append(
        f"- Tests conducted in: {api_client_geo}"
    )
    lines.append(
        "- Cache behavior is provider-dependent"
    )
    lines.append("=" * 72)

    output = "\n".join(lines) + "\n"
    if results_file:
        results_file.write(output)
    else:
        print(output)


def _display_results(
        results: Dict[int, Dict[str, Any]],
        cost_summary: str,
        results_file: Optional[Any] = None,
) -> None:
    """
    Display benchmark results in a formatted table.
    """
    lines = []
    lines.append("="*80)
    lines.append("RESULTS")
    lines.append("="*80)
    lines.append("")

    lines.append(
        f"{'Prompt Size':<15} {'Mean TTFT':<15} "
        f"{'Median TTFT':<15} "
        f"{'Min':<10} {'Max':<10} {'StdDev':<10}")
    lines.append("-"*80)

    for size in sorted(results.keys()):
        r = results[size]
        lines.append(
            f"{size:<15} "
            f"{r['mean']:.3f}s{'':<8} "
            f"{r['median']:.3f}s{'':<8} "
            f"{r['min']:.3f}s{'':<3} "
            f"{r['max']:.3f}s{'':<3} "
            f"{r['stdev']:.3f}s")

    lines.append("")
    lines.append("="*80)
    lines.append("PER-TOKEN LATENCY ANALYSIS")
    lines.append("="*80)
    lines.append("")

    # Calculate per-token latency between
    # prompt sizes
    sorted_sizes = sorted(results.keys())

    if len(sorted_sizes) >= 2:
        lines.append(
            "Calculating per-token latency "
            "between consecutive prompt sizes:"
        )
        lines.append("")

        per_token_rates = []

        for i in range(len(sorted_sizes) - 1):
            size1 = sorted_sizes[i]
            size2 = sorted_sizes[i+1]
            ttft1 = results[size1]["mean"]
            ttft2 = results[size2]["mean"]

            delta_tokens = size2 - size1
            delta_time = (
                (ttft2 - ttft1) * 1000
            )

            ms_per_token = (
                delta_time / delta_tokens
            )
            per_token_rates.append(ms_per_token)

            lines.append(
                f"{size1:>5} -> {size2:>5} "
                f"tokens: {delta_time:>7.1f}ms"
                f" / {delta_tokens:>4} tokens"
                f" = {ms_per_token:.3f} "
                f"ms/token"
            )

        lines.append("")
        avg_rate = statistics.mean(
            per_token_rates
        )
        lines.append(
            f"Average per-token latency: "
            f"{avg_rate:.3f} ms/token"
        )
        lines.append(
            f"Range: "
            f"{min(per_token_rates):.3f} - "
            f"{max(per_token_rates):.3f} "
            f"ms/token"
        )

    # Append cost summary if available
    if cost_summary and len(cost_summary) > 0:
        lines.append(cost_summary)

    # Add notes about the benchmark
    lines.append("")
    lines.append("="*80)
    lines.append("NOTES")
    lines.append("="*80)

    provider = _identify_provider()
    model_region = _get_model_deployment_region()
    model_desc_str = (
        f"{MODEL} via {provider}{model_region}"
    )

    api_client_geo = os.getenv(
        "API_CLIENT_GEOGRAPHY", "Singapore"
    )

    lines.append(
        "- Measurements include network latency"
    )
    if provider == "OpenRouter":
        lines.append(
            "- Infrastructure: OpenRouter "
            "shared API "
            "(not dedicated capacity)"
        )
    else:
        lines.append(
            "- Infrastructure: "
            "Configured API endpoint"
        )
    lines.append(
        "- Results will vary based on "
        "server load and network conditions"
    )
    lines.append(f"- Model: {model_desc_str}")
    lines.append(
        f"- Tests conducted in: "
        f"{api_client_geo}"
    )
    lines.append("="*80)

    output = "\n".join(lines) + "\n"
    if results_file:
        results_file.write(output)
    else:
        print(output)


def _measure_ttft(
    *,
    run_id: str,
    prompt: str,
    output_file: Optional[Any] = None,
) -> Optional[float]:
    """
    Measure Time to First Token for a given prompt.
    Returns TTFT in seconds.
    """
    try:
        # Log request metadata (avoid logging full prompt if sensitive)
        _log_request(prompt=prompt, run_id=run_id)

        # Keep track of the start time
        start_time = time.time()
        ttft: Optional[float] = None
        full_text = ""

        with client.responses.stream(
            model=MODEL,
            input=[{"role": "user", "content": prompt}],
        ) as stream:
            for event in stream:
                log.info(
                    "stream_event",
                    event_type=event.type,
                    run_id=run_id,
                )

                # FIRST TOKEN — this is the TTFT moment
                if event.type == "response.output_text.delta":
                    if ttft is None:
                        ttft = time.time() - start_time
                        log.info(
                            "first_token_received",
                            ttft=ttft,
                            run_id=run_id,
                        )

                    full_text += event.delta
                    _log_response(
                        is_chunk=True,
                        chunk=event.delta,
                        response=None,
                        run_id=run_id,
                    )

                # Final response + usage
                if event.type == "response.completed":
                    _log_response(
                        is_chunk=False,
                        chunk=None,
                        response=event.response,
                        run_id=run_id,
                    )

        return ttft

    except Exception as e:
        msg = f"TTFT measurement failed: {e}"
        log.warning("ttft_error", error=str(e), run_id=run_id)
        if output_file:
            output_file.write(msg + "\n")
        else:
            print(msg)
        return None


def _measure_ttft_with_prefix(
    *,
    run_id: str,
    system_prompt: str,
    user_query: str,
    output_file: Optional[Any] = None,
) -> Tuple[Optional[float], int]:
    """
    Measure TTFT using a system prompt (via the
    instructions parameter) and a user query.
    Returns (ttft_seconds, cached_tokens).
    """
    cached_tokens = 0
    try:
        _log_request(
            prompt=user_query,
            run_id=run_id,
        )

        start_time = time.time()
        ttft: Optional[float] = None

        with client.responses.stream(
            model=MODEL,
            instructions=system_prompt,
            input=user_query,
        ) as stream:
            for event in stream:
                log.info(
                    "stream_event",
                    event_type=event.type,
                    run_id=run_id,
                )

                if (
                    event.type
                    == "response.output_text.delta"
                ):
                    if ttft is None:
                        ttft = (
                            time.time() - start_time
                        )
                        log.info(
                            "first_token_received",
                            ttft=ttft,
                            run_id=run_id,
                        )

                if (
                    event.type
                    == "response.completed"
                ):
                    resp = event.response
                    usage = resp.usage
                    if (
                        hasattr(
                            usage,
                            "input_tokens_details",
                        )
                        and (
                            usage
                            .input_tokens_details
                        )
                    ):
                        cached_tokens = getattr(
                            usage
                            .input_tokens_details,
                            "cached_tokens",
                            0,
                        ) or 0

                    _log_response(
                        is_chunk=False,
                        chunk=None,
                        response=resp,
                        run_id=run_id,
                    )
                    log.info(
                        "cache_info",
                        run_id=run_id,
                        cached_tokens=(
                            cached_tokens
                        ),
                    )

        return ttft, cached_tokens

    except (RateLimitError, APIError) as e:
        if (
            isinstance(e, RateLimitError)
            or "Too Many Requests" in str(e)
        ):
            raise  # Let caller handle retry
        msg = f"TTFT measurement failed: {e}"
        log.warning(
            "ttft_error",
            error=str(e),
            run_id=run_id,
        )
        if output_file:
            output_file.write(msg + "\n")
        else:
            print(msg)
        return None, 0


def run_benchmark(
        results_file: Any,
        benchmark_run_dt: datetime) -> Tuple[Dict[int, Dict[str, Any]], str]:
    """
    Run the complete benchmark across all prompt sizes.
    """

    # Get information about the proivider and region where model endpoint is
    provider = _identify_provider()
    model_region = _get_model_deployment_region()
    model_desc_str = f"{MODEL} via {provider}{model_region}"

    header = (
        "="*80 + "\nTTFT Micro-Benchmark\n" +
        f"Model: {model_desc_str}\n" +
        f"Iterations per size: {ITERATIONS_PER_SIZE}\n" +
        f"Sleep between requests: {SLEEP_SECONDS_BETWEEN_REQUESTS} seconds\n" +
        f"Benchmark Run Start Date & Time: {benchmark_run_dt.isoformat()}\n" +
        "="*80 + "\n\n"
    )
    if results_file:
        results_file.write(header)
    # Always print header to console
    print(header)

    results: Dict[int, Dict[str, Any]] = {}

    run_id_base = _generate_run_id()
    for size in PROMPT_SIZES:
        run_id = f"{run_id_base}-{size}"
        size_line = f"Testing prompt size: ~{size} tokens. Run ID: {run_id}\n"
        if results_file:
            results_file.write(size_line)
        prompt = _generate_prompt(size)
        ttfts = []

        print(f"Testing ~{size} tokens (run_id={run_id})")

        for i in range(ITERATIONS_PER_SIZE):
            run_line = f"  Run {i+1}/{ITERATIONS_PER_SIZE}... "
            if results_file:
                results_file.write(run_line)
            # Always show run progress on console
            print(run_line, end="")

            ttft = _measure_ttft(
                prompt=prompt,
                output_file=results_file,
                run_id=run_id)

            if ttft is not None:
                ttfts.append(ttft)
                result_line = f"{ttft:.3f}s\n"
            else:
                result_line = "FAILED (TTFT not captured)\n"

            if results_file:
                results_file.write(result_line)
            # Always show result on console
            print(result_line, end="")

            # Small delay between requests to avoid rate limiting
            time.sleep(SLEEP_SECONDS_BETWEEN_REQUESTS)

        if ttfts:
            results[size] = {
                "mean": statistics.mean(ttfts),
                "median": statistics.median(ttfts),
                "min": min(ttfts),
                "max": max(ttfts),
                "stdev": statistics.stdev(ttfts) if len(ttfts) > 1 else 0.0,
                "measurements": ttfts,
            }

        if results_file:
            results_file.write("\n")
        else:
            print()

    return results, run_id_base


def run_prefix_caching_benchmark(
        results_file: Any,
        benchmark_run_dt: datetime,
) -> Tuple[Dict[int, Dict[str, Any]], str]:
    """
    Run prefix caching benchmark. For each
    prefix size, measures TTFT for a cache-miss
    (1st request) and cache-hits (subsequent
    requests with same system prompt prefix).
    """
    provider = _identify_provider()
    model_region = _get_model_deployment_region()
    model_str = (
        f"{MODEL} via {provider}{model_region}"
    )

    prefix_sizes_str = ", ".join(
        str(s) for s in PREFIX_SIZES
    )
    header = (
        "=" * 72 + "\n"
        "PREFIX CACHING TTFT BENCHMARK\n"
        f"Model: {model_str}\n"
        f"Prefix sizes: {prefix_sizes_str} tokens\n"
        f"Cache-hit iterations per size: "
        f"{CACHE_HIT_ITERATIONS}\n"
        "Warmup: 1 throwaway request\n"
        f"Benchmark start: "
        f"{benchmark_run_dt.isoformat()}\n"
        + "=" * 72 + "\n\n"
    )
    if results_file:
        results_file.write(header)
    print(header)

    # Validate that all prefix sizes exceed
    # the minimum caching threshold (1,024
    # tokens for OpenAI automatic caching).
    min_cache_threshold = 1024
    for ps in PREFIX_SIZES:
        if ps < min_cache_threshold:
            msg = (
                f"Prefix size {ps} is below "
                f"the minimum caching threshold"
                f" ({min_cache_threshold} "
                f"tokens). OpenAI requires "
                f">= {min_cache_threshold} "
                f"tokens for automatic caching."
            )
            print(f"WARNING: {msg}")
            log.warning(
                "prefix_below_threshold",
                prefix_size=ps,
                threshold=min_cache_threshold,
            )

    results: Dict[int, Dict[str, Any]] = {}
    run_id_base = _generate_run_id()
    rate_limiter = AdaptiveRateLimiter()

    for prefix_size in PREFIX_SIZES:
        sys_prompt = _generate_system_prompt(
            prefix_size
        )
        queries = _generate_user_queries(
            CACHE_HIT_ITERATIONS + 1
        )

        size_hdr = (
            f"\nTesting prefix size: "
            f"~{prefix_size} tokens\n"
        )
        if results_file:
            results_file.write(size_hdr)
        print(size_hdr, end="")

        # Warmup: throwaway request to warm
        # connection (separate from cache-miss)
        warmup_id = (
            f"{run_id_base}-{prefix_size}-warmup"
        )
        warmup_prompt = _generate_prompt(100)
        print("  Warmup... ", end="")
        if results_file:
            results_file.write("  Warmup... ")

        warmup_ttft = _measure_ttft(
            run_id=warmup_id,
            prompt=warmup_prompt,
        )
        warmup_str = (
            f"{warmup_ttft:.3f}s (discarded)\n"
            if warmup_ttft
            else "FAILED (discarded)\n"
        )
        print(warmup_str, end="")
        if results_file:
            results_file.write(warmup_str)

        rate_limiter.wait()

        # Cache-miss: measure N times with
        # distinct system prompts to get
        # baseline variance
        miss_ttfts: List[float] = []
        for m in range(CACHE_MISS_ITERATIONS):
            miss_id = (
                f"{run_id_base}"
                f"-{prefix_size}-miss-{m+1}"
            )
            miss_sys = _generate_system_prompt(
                prefix_size + m * 50
            )
            run_str = (
                f"  Cache miss "
                f"{m+1}/"
                f"{CACHE_MISS_ITERATIONS}... "
            )
            print(run_str, end="")
            if results_file:
                results_file.write(run_str)

            try:
                miss_ttft, miss_cached = (
                    rate_limiter.execute_with_retry(
                        _measure_ttft_with_prefix,
                        run_id=miss_id,
                        system_prompt=miss_sys,
                        user_query=queries[0],
                        output_file=results_file,
                    )
                )
            except (RateLimitError, APIError):
                miss_ttft, miss_cached = None, 0

            miss_line = (
                f"{miss_ttft:.3f}s"
                f"  cached_tokens: "
                f"{miss_cached}\n"
                if miss_ttft
                else "FAILED\n"
            )
            print(miss_line, end="")
            if results_file:
                results_file.write(miss_line)

            if miss_ttft is not None:
                miss_ttfts.append(miss_ttft)

            rate_limiter.wait()

        miss_mean = (
            statistics.mean(miss_ttfts)
            if miss_ttfts else None
        )

        # Cache-hits: subsequent requests with
        # same system prompt
        hit_ttfts: List[float] = []
        hit_cached_list: List[int] = []

        for i, query in enumerate(
            queries[1:], start=1
        ):
            hit_id = (
                f"{run_id_base}"
                f"-{prefix_size}-hit-{i}"
            )
            run_str = (
                f"  Cache hit "
                f"{i}/{CACHE_HIT_ITERATIONS}... "
            )
            print(run_str, end="")
            if results_file:
                results_file.write(run_str)

            try:
                hit_ttft, hit_cached = (
                    rate_limiter.execute_with_retry(
                        _measure_ttft_with_prefix,
                        run_id=hit_id,
                        system_prompt=sys_prompt,
                        user_query=query,
                        output_file=results_file,
                    )
                )
            except (RateLimitError, APIError):
                hit_ttft, hit_cached = None, 0

            if hit_ttft is not None:
                hit_ttfts.append(hit_ttft)
                hit_cached_list.append(hit_cached)
                hit_line = (
                    f"{hit_ttft:.3f}s"
                    f"  cached_tokens: "
                    f"{hit_cached}\n"
                )
            else:
                hit_cached_list.append(0)
                hit_line = "FAILED\n"

            print(hit_line, end="")
            if results_file:
                results_file.write(hit_line)

            rate_limiter.wait()

        # Collect results for this prefix size
        results[prefix_size] = {
            "miss": {
                "ttft": miss_mean,
                "ttfts": miss_ttfts,
                "stdev": (
                    statistics.stdev(miss_ttfts)
                    if len(miss_ttfts) > 1
                    else 0.0
                ),
            },
            "hits": {
                "ttfts": hit_ttfts,
                "cached_tokens": hit_cached_list,
                "mean": (
                    statistics.mean(hit_ttfts)
                    if hit_ttfts else None
                ),
                "median": (
                    statistics.median(hit_ttfts)
                    if hit_ttfts else None
                ),
                "p50": (
                    _percentile(hit_ttfts, 50)
                    if hit_ttfts else None
                ),
                "p95": (
                    _percentile(hit_ttfts, 95)
                    if hit_ttfts else None
                ),
                "min": (
                    min(hit_ttfts)
                    if hit_ttfts else None
                ),
                "max": (
                    max(hit_ttfts)
                    if hit_ttfts else None
                ),
                "stdev": (
                    statistics.stdev(hit_ttfts)
                    if len(hit_ttfts) > 1
                    else 0.0
                ),
            },
        }

        if results_file:
            results_file.write("\n")
        print()

    # Print adaptive rate limiter summary
    rl_summary = rate_limiter.summary()
    rl_line = (
        f"\nAdaptive rate limiting: "
        f"{rl_summary}\n"
    )
    print(rl_line, end="")
    if results_file:
        results_file.write(rl_line)

    return results, run_id_base


